package entity;

public class FormPageBean {

    private String check;

    private String radio;

    private Category checks;

    public String getCheck() {
        return check;
    }

    public void setCheck(String check) {
        this.check = check;
    }

    public String getRadio() {
        return radio;
    }

    public void setRadio(String radio) {
        this.radio = radio;
    }

    public Category getChecks() {
        return checks;
    }

    public void setChecks(Category checks) {
        this.checks = checks;
    }
}
